puts " Input a month:"
months = gets.chomp

case months
when "January"
  puts "Winter"
when "February"
  puts "Winter"
when "March"
  puts "Spring"
when "April"
  puts "Spring"
when "May"
  puts "Spring"
when "June"
  puts "Summer"
when "July"
  puts "Summer"
when "August"
  puts "Summer"
when "September"
  puts "Fall"
when "October"
  puts "Fall"
when "November"
  puts "Fall"
when "December"
  puts "Winter"
end
